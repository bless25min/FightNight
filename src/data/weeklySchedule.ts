const recommendedWeeklyCourseIdSet = new Set<string>(
  RECOMMENDED_WEEKLY_COURSE_IDS,
)
const weekdayLabels = new Set(['一', '二', '三', '四', '五'])

function getBaseWeeklyCourseId(courseId: string) {
  return courseId.replace(/-\d{4}-\d{2}-\d{2}$/, '')
}

export function isRecommendedWeeklyCourse(course: WeeklyCourse) {
  return recommendedWeeklyCourseIdSet.has(getBaseWeeklyCourseId(course.id))
}

function getStartHour(time: string) {
  return Number(time.slice(0, 2))
}

function isWeekdayCourse(course: WeeklyCourse) {
  return weekdayLabels.has(course.weekday)
}

function isEveningCourse(course: WeeklyCourse) {
  return getStartHour(course.startTime) >= 18
}

function isBoxingOrMuayThaiCourse(course: WeeklyCourse) {
  const name = course.name
  return (
    name.includes('拳擊') || name.includes('泰拳') || name.includes('踢拳')
  )
}

function isBasicCourse(course: WeeklyCourse) {
  return course.name.includes('基礎')
}

function isTechniqueCourse(course: WeeklyCourse) {
  return course.name.includes('技巧')
}

function isConditioningCourse(course: WeeklyCourse) {
  const name = course.name
  return name.includes('體適能') || name.includes('戰鬥體適能')
}

function isFightNightCourse(course: WeeklyCourse) {
  return (
    isWeekdayCourse(course) &&
    isEveningCourse(course) &&
    isConditioningCourse(course)
  )
}

function isBootCampCourse(course: WeeklyCourse) {
  return (
    isBoxingOrMuayThaiCourse(course) &&
    (isBasicCourse(course) || isTechniqueCourse(course))
  )
}

export function isPublicWeeklyCourse(course: WeeklyCourse) {
  return isFightNightCourse(course)
}

export function isWeeklyCourseAvailableForCategory(
  course: WeeklyCourse,
  category: CourseCategory,
) {
  return category === 'FIGHT_NIGHT'
    ? isFightNightCourse(course)
    : isBootCampCourse(course)
}

export function getWeeklyCourseForCategory(
  course: WeeklyCourse,
  category: CourseCategory,
) {
  return {
    ...course,
    category,
  }
}

